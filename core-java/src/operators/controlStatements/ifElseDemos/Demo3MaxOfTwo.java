package operators.controlStatements.ifElseDemos;

public class Demo3MaxOfTwo {
    public static void main(String[] args) {
        int number1 = 11;
        int number2 = 10;

        if (number1 > number2) {
            System.out.println(number1 + " is max");
        }
        if (number2 > number1) {
            System.out.println(number2 + " is max");
        }
    }
}
