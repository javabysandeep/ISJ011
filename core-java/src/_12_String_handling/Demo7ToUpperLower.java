package _12_String_handling;

public class Demo7ToUpperLower {
    public static void main(String[] args) {
        String s1 = "ABC";
        s1.toLowerCase();
        System.out.println(s1);//ABC

        String s2 = "ABC";
        s2 = s2.toLowerCase();
        System.out.println(s2);//abc

    }
}
