package controlStatements.ifElseDemos;

public class Demo2 {
    public static void main(String[] args) {
        int number = 11;
        if (number % 2 == 0) {
            System.out.println(number + " is an even number");
        }
        if (number % 2 == 1) {
            System.out.println(number + " is an odd number");
        }
    }
}
