package _2_dataTypes;

public class DataType1 {

	public static void main(String[] args){

		byte value1 = 127;
		System.out.println("Byte value stored "+value1);

		short value2 = 32767;
		System.out.println("Short value stored "+value2);

		int value3 = 2147483647;
		System.out.println("Int value stored "+value3);

		long value4 = 9223372036854775807L;
		System.out.println("long value stored "+value4);

		float value5 = 123.45F;
		System.out.println("Float value stored "+value5+"\t"+(Float.MAX_VALUE-1));

		double value6 = 123.45D;
		System.out.println("Double value stored "+value6);


		char value7 = 'A';
		System.out.println("char value stored "+value7);


		char value8 = 65;
		System.out.println("char value stored "+value8);

		boolean value9 = false;
		System.out.println("boolean value stored "+value9);

	}

} 












